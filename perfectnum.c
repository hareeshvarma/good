#include <stdio.h>
#include <math.h>

int main()
{
    int num,m,sum;
    int iVar;
    float fVar;

    printf("Enter an integer number: ");
    scanf("%d",&num);

    fVar=sqrt((double)sum);
    sum=num*m;
    iVar=fVar;

    if(iVar==fVar)
        printf("yes");
    else
        printf("no");

    return 0;
}
